fn my_first_interpreter(code: &str) -> String {
    code.chars()
        .fold((0u8, String::new()), |mut acc, c| {
            match c {
                '+' => (acc.0.wrapping_add(1), acc.1),
                '.' => (acc.0, format!("{}{}",acc.1, acc.0 as char)),
                _ => acc,
            }
        })
        .1
}

#[cfg(test)]
